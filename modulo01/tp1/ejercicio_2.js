let A, B;
A = 9;
B = 7;
//declaramos dos variables A B y les asignamos los valores 9 y 7 respectivamente
resultado = A + B;
//la suma de A y B se guarda en la variable "resultado"
alert (`La suma de A+B es: ${resultado}`);
alert (`La diferencia de A-B es: ${A-B}`);
alert (`La multiplicación de AxB es: ${A*B}`);
alert (`La división de A/B es: ${resultado=A/B}`);
//con la funcion "alert" se muestra en pantalla el resultado de la suma, resta, multiplicacion y division entre A y B
